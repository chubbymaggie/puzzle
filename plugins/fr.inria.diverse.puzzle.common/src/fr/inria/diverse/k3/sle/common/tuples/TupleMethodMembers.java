package fr.inria.diverse.k3.sle.common.tuples;

import java.util.ArrayList;

import org.eclipse.xtext.common.types.JvmOperation;

public class TupleMethodMembers {

	// -----------------------------------------------
	// Attributes
	// -----------------------------------------------
	
	private JvmOperation method;
	private ArrayList<String> members;
	
	// -----------------------------------------------
	// Constructor
	// -----------------------------------------------
	
	public TupleMethodMembers(JvmOperation method) {
		this.method = method;
		this.members = new ArrayList<String>();
	}
	
	// -----------------------------------------------
	// Getters and setters
	// -----------------------------------------------
	
	public JvmOperation getMethod() {
		return method;
	}
	
	public void setMethod(JvmOperation method) {
		this.method = method;
	}
	
	public ArrayList<String> getMembers() {
		return members;
	}
}