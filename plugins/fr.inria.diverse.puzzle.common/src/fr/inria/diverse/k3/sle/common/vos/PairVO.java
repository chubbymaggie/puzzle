package fr.inria.diverse.k3.sle.common.vos;

public class PairVO {

	private int a;
	private int b;
	
	public PairVO(int a, int b){
		this.a = a;
		this.b = b;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}
}
